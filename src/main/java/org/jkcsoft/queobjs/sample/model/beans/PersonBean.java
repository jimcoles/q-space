package org.jkcsoft.queobjs.sample.model.beans;

public class PersonBean {
	private String first;
	private String last;
	public PersonBean(String first, String last) {
		super();
		this.first = first;
		this.last = last;
	}
	public String getFirst() {
		return first;
	}
	public String getLast() {
		return last;
	}
	
	
	
}
